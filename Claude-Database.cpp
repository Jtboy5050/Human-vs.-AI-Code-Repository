#include <iostream>
#include <vector>
#include <string>
#include <limits>

#ifdef _WIN32
#define CLEAR_SCREEN "cls"
#else
#define CLEAR_SCREEN "clear"
#endif

// ??? Structs ????????????????????????????????????????????????????????????????

struct Teacher {
    std::string name;
    int         age;
    std::string subject;
    int         id;
};

struct Student {
    std::string name;
    int         age;
    int         id;
    std::string grade; // e.g. "A", "B+", "92"
};

// ??? Globals ????????????????????????????????????????????????????????????????

std::vector<Teacher> teachers;
std::vector<Student> students;

// ??? Helpers ????????????????????????????????????????????????????????????????

// Flush any leftover newline so getline() won't read an empty string
void flushInput() {
    std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
}

void clearScreen() {
    system(CLEAR_SCREEN);
}

// Check whether a given ID is already used by any teacher or student
bool idExists(int id) {
    for (const auto& t : teachers)
        if (t.id == id) return true;
    for (const auto& s : students)
        if (s.id == id) return true;
    return false;
}

// Prompt the user for a unique positive ID
int getUniqueID() {
    int id;
    while (true) {
        std::cout << "  Enter ID: ";
        std::cin >> id;
        if (std::cin.fail() || id <= 0) {
            std::cin.clear();
            flushInput();
            std::cout << "  Invalid — please enter a positive integer.\n";
        }
        else if (idExists(id)) {
            std::cout << "  That ID is already taken. Try another.\n";
            flushInput();
        }
        else {
            flushInput();
            return id;
        }
    }
}

// ??? Display ????????????????????????????????????????????????????????????????

void displayAll() {
    clearScreen();

    std::cout << "????????????????????????????????????????????????\n";
    std::cout << "?              DATABASE ENTRIES                ?\n";
    std::cout << "????????????????????????????????????????????????\n\n";

    // ?? Teachers ??
    std::cout << "TEACHERS (" << teachers.size() << ")\n";
    std::cout << "????????????????????????????????????????????????\n";
    if (teachers.empty()) {
        std::cout << "  (none)\n";
    }
    else {
        for (const auto& t : teachers) {
            std::cout << "  [ID " << t.id << "]  " << t.name
                << "  |  Age: " << t.age
                << "  |  Subject: " << t.subject << "\n";
        }
    }

    // ?? Students ??
    std::cout << "\nSTUDENTS (" << students.size() << ")\n";
    std::cout << "????????????????????????????????????????????????\n";
    if (students.empty()) {
        std::cout << "  (none)\n";
    }
    else {
        for (const auto& s : students) {
            std::cout << "  [ID " << s.id << "]  " << s.name
                << "  |  Age: " << s.age
                << "  |  Grade: " << s.grade << "\n";
        }
    }

    std::cout << "\n";
}

// ??? Add ????????????????????????????????????????????????????????????????????

void addTeacher() {
    clearScreen();
    std::cout << "?? Add Teacher ??????????????????????????????????\n";

    Teacher t;
    std::cout << "  Name: ";
    std::getline(std::cin, t.name);

    std::cout << "  Age: ";
    std::cin >> t.age;
    flushInput();

    std::cout << "  Subject: ";
    std::getline(std::cin, t.subject);

    t.id = getUniqueID();

    teachers.push_back(t);
    std::cout << "\n  Teacher added successfully.\n";
}

void addStudent() {
    clearScreen();
    std::cout << "?? Add Student ??????????????????????????????????\n";

    Student s;
    std::cout << "  Name: ";
    std::getline(std::cin, s.name);

    std::cout << "  Age: ";
    std::cin >> s.age;
    flushInput();

    std::cout << "  Grade (e.g. A, B+, 92): ";
    std::getline(std::cin, s.grade);

    s.id = getUniqueID();

    students.push_back(s);
    std::cout << "\n  Student added successfully.\n";
}

// ??? Delete ?????????????????????????????????????????????????????????????????

void deleteEntry() {
    clearScreen();
    std::cout << "?? Delete Entry ?????????????????????????????????\n";
    std::cout << "  Enter the ID of the entry to delete: ";

    int id;
    std::cin >> id;
    flushInput();

    // Search teachers first
    for (auto it = teachers.begin(); it != teachers.end(); ++it) {
        if (it->id == id) {
            std::cout << "\n  Deleted teacher: " << it->name << "\n";
            teachers.erase(it);
            return;
        }
    }

    // Then search students
    for (auto it = students.begin(); it != students.end(); ++it) {
        if (it->id == id) {
            std::cout << "\n  Deleted student: " << it->name << "\n";
            students.erase(it);
            return;
        }
    }

    std::cout << "\n  No entry found with ID " << id << ".\n";
}

// ??? Menu ????????????????????????????????????????????????????????????????????

void showMenu() {
    std::cout << "????????????????????????????????????????????????\n";
    std::cout << "?           SCHOOL DATABASE SYSTEM             ?\n";
    std::cout << "????????????????????????????????????????????????\n\n";
    std::cout << "  1.  See all entries\n";
    std::cout << "  2.  Add a student\n";
    std::cout << "  3.  Add a teacher\n";
    std::cout << "  4.  Delete an entry\n";
    std::cout << "  5.  Exit\n\n";
    std::cout << "  Choice: ";
}

// ??? Main ???????????????????????????????????????????????????????????????????

int main() {
    int choice;

    while (true) {
        clearScreen();
        showMenu();

        std::cin >> choice;
        flushInput();

        switch (choice) {
        case 1: displayAll();   break;
        case 2: addStudent();   break;
        case 3: addTeacher();   break;
        case 4: deleteEntry();  break;
        case 5:
            clearScreen();
            std::cout << "  Goodbye!\n\n";
            return 0;
        default:
            std::cout << "\n  Invalid option. Please enter 1–5.\n";
            break;
        }

        // Pause so the user can read the result before the screen clears
        std::cout << "\n  Press Enter to return to menu...";
        std::cin.get();
    }
}